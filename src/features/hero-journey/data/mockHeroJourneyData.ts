const buildObjectives = (
  completedCount: number,
): HeroJourneyStudentProgress["currentObjectives"] => [
  {
    id: "warm-up-checkpoint",
    titleEn: "Warm-up checkpoint",
    titleAr: "نقطة التهيئة",
    isCompleted: completedCount >= 1,
  },
  {
    id: "core-mission-step",
    titleEn: "Core mission step",
    titleAr: "خطوة المهمة الأساسية",
    isCompleted: completedCount >= 2,
  },
  {
    id: "reflection-submission",
    titleEn: "Reflection submission",
    titleAr: "تسليم الانعكاس",
    isCompleted: completedCount >= 3,
  },
];

const createStudent = (
  student: Omit<HeroJourneyStudentProgress, "currentObjectives"> & {
    currentObjectivesCompleted: number;
  },
): HeroJourneyStudentProgress => ({
  ...student,
  currentObjectives: buildObjectives(student.currentObjectivesCompleted),
});

